// 繁体字转简体字转换函数
function convertToSimplified(text) {
    if (!text) return text;
    
    let result = '';
    for (let i = 0; i < text.length; i++) {
        const char = text[i];
        result += traditionalToSimplified[char] || char;
    }
    return result;
}

